//go:build ignore

// Command gen extracts enum values from the Streams OpenAPI spec and writes
// them to enums.gen.go, keeping the provider's validators in sync with the
// API. It is invoked via go:generate in streams.go and runs as part of
// `make vendor`.
package main

import (
	"bytes"
	"encoding/json"
	"go/format"
	"log"
	"os"
	"text/template"
)

const (
	specFile = "streams-openapi.json"
	outFile  = "enums.gen.go"
	// dtoName is the schema whose properties define the create-time input
	// enums. Read-only status values (terminated, completed) deliberately do
	// not live here, so those validators stay hand-maintained.
	dtoName = "CreateStreamDto"
)

// enums maps each generated Go variable to the CreateStreamDto property whose
// enum it mirrors. Add an entry here to surface another spec enum.
var enums = []struct {
	Var, Prop, Doc string
}{
	{"Networks", "network", "valid stream network values"},
	{"Datasets", "dataset", "valid stream dataset values"},
	{"Destinations", "destination", "valid stream destination types"},
	{"Regions", "region", "valid stream region values"},
}

type spec struct {
	Components struct {
		Schemas map[string]struct {
			Properties map[string]struct {
				Enum []string `json:"enum"`
			} `json:"properties"`
		} `json:"schemas"`
	} `json:"components"`
}

var tmpl = template.Must(template.New("enums").Parse(`// Copyright 2025 Circle Internet Group, Inc.  All rights reserved.
//
// Licensed under the Apache License, Version 2.0 (the "License");
// you may not use this file except in compliance with the License.
// You may obtain a copy of the License at
//
//     http://www.apache.org/licenses/LICENSE-2.0
//
// Unless required by applicable law or agreed to in writing, software
// distributed under the License is distributed on an "AS IS" BASIS,
// WITHOUT WARRANTIES OR CONDITIONS OF ANY KIND, either express or implied.
// See the License for the specific language governing permissions and
// limitations under the License.
//
// SPDX-License-Identifier: Apache-2.0

// Code generated by gen/generate.go from {{.Spec}}; DO NOT EDIT.

package streams
{{range .Enums}}
// {{.Var}} are the {{.Doc}} accepted by the Streams API.
var {{.Var}} = []string{
{{- range .Values}}
	{{printf "%q" .}},
{{- end}}
}
{{end}}`))

func main() {
	raw, err := os.ReadFile(specFile)
	if err != nil {
		log.Fatalf("read spec: %v", err)
	}

	var s spec
	if err := json.Unmarshal(raw, &s); err != nil {
		log.Fatalf("parse spec: %v", err)
	}

	dto, ok := s.Components.Schemas[dtoName]
	if !ok {
		log.Fatalf("schema %q not found in %s", dtoName, specFile)
	}

	type enumData struct {
		Var, Doc string
		Values   []string
	}
	data := struct {
		Spec  string
		Enums []enumData
	}{Spec: specFile}

	for _, e := range enums {
		prop, ok := dto.Properties[e.Prop]
		if !ok || len(prop.Enum) == 0 {
			log.Fatalf("%s.%s has no enum in %s", dtoName, e.Prop, specFile)
		}
		data.Enums = append(data.Enums, enumData{Var: e.Var, Doc: e.Doc, Values: prop.Enum})
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		log.Fatalf("render: %v", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("gofmt: %v\n%s", err, buf.String())
	}

	if err := os.WriteFile(outFile, formatted, 0o644); err != nil {
		log.Fatalf("write %s: %v", outFile, err)
	}
}
